//! - Price/time axes
//! - Command palette (~ tilde, Quake-style console)
//! - Symbol/timeframe selector
//! - MTF grid mode
//! - Floating windows: Settings, Risk, Backtest, Screener, News, etc.
//! - Right panel: positions, orders, risk
//! - Bottom panel: log messages

